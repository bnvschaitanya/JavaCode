public class Literals {
    public static void main(String[] args) {
        byte b = 1;
        short s = 23;
        int i = 234;
        long l = 100l;
        float f = 1.1f;
        double d = 1.2d;
        char c = 'a';
        boolean bool = true;
        System.out.println("Welcome " + args[0]);
        System.out.println("Byte b:" + b + " Short s:"+ s + " Integer i:"+ i);
        System.out.println("Float f:" + f + " Double d:" + d);
        System.out.println("Character c:" + c + " Boolean bool: "+bool);
    }
}
